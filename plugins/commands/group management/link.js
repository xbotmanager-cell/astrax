/**
 * AstraX - plugins/commands/group management/link.js
 * Group Link Command - Get group invite link
 * No admin check from router - handles 403/401 errors directly
 * Category: group management
 */

export default {
  name: 'link',
  alias: ['grouplink', 'invite', 'getlink'],
  desc: 'Get group invite link',
  category: 'group management',
  usage: 'link',
  permission: 'isOwner',

  async execute(sock, m, args, { db, logger, contextInfo, from, isGroup }) {
    try {
      // в”Җв”Җв”Җ GET PREFIX FROM DB в”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җ
      const prefix = await db.get('prefix') || '.'

      // в”Җв”Җв”Җ CHECK IF GROUP в”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җ
      if (!isGroup) {
        const errorText = `
в•ӯв”Җв”Җв”Җв”Җв”ҖгҖ” ERROR гҖ•в”Җв”Җв”Җв”Җв”Җв”ҲвҠ·
в”Ӯ в—Ұвһӣ Group command only
в•°в”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”ҖвҠ·
`
        return await sock.sendMessage(from, {
          text: errorText.trim(),
          contextInfo
        }, { quoted: m })
      }

      // в”Җв”Җв”Җ GET GROUP METADATA в”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җ
      let groupMetadata
      try {
        groupMetadata = await sock.groupMetadata(from)
      } catch (e) {
        const errorText = `
в•ӯв”Җв”Җв”Җв”Җв”ҖгҖ” ERROR 403 гҖ•в”Җв”Җв”Җв”Җв”Җв”ҲвҠ·
в”Ӯ в—Ұвһӣ Bot needs to be in group
в•°в”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”ҖвҠ·
`
        return await sock.sendMessage(from, {
          text: errorText.trim(),
          contextInfo
        }, { quoted: m })
      }

      // в”Җв”Җв”Җ GET INVITE CODE в”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җ
      let inviteCode
      try {
        inviteCode = await sock.groupInviteCode(from)
      } catch (e) {
        const errMsg = e.message || e.toString()
        let errorText = ''

        // 403 - Bot not admin
        if (errMsg.includes('403') || errMsg.includes('forbidden')) {
          errorText = `
в•ӯв”Җв”Җв”Җв”Җв”ҖгҖ” ERROR 403 гҖ•в”Җв”Җв”Җв”Җв”Җв”ҲвҠ·
в”Ӯ в—Ұвһӣ Bot needs admin rights
в•°в”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”ҖвҠ·
`
        }
        // 401 - Not admin
        else if (errMsg.includes('401') || errMsg.includes('not-authorized')) {
          errorText = `
в•ӯв”Җв”Җв”Җв”Җв”ҖгҖ” ERROR 401 гҖ•в”Җв”Җв”Җв”Җв”Җв”ҲвҠ·
в”Ӯ в—Ұвһӣ You need admin rights
в•°в”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”ҖвҠ·
`
        }
        else {
          errorText = `
в•ӯв”Җв”Җв”Җв”Җв”ҖгҖ” ERROR гҖ•в”Җв”Җв”Җв”Җв”Җв”ҲвҠ·
в”Ӯ в—Ұвһӣ Failed to get link
в•°в”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”ҖвҠ·
`
        }

        logger.error('LINK', 'Failed to get invite code', errMsg)
        return await sock.sendMessage(from, {
          text: errorText.trim(),
          contextInfo
        }, { quoted: m })
      }

      // в”Җв”Җв”Җ BUILD RESULT MESSAGE в”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җ
      const groupLink = `https://chat.whatsapp.com/${inviteCode}`
      const resultText = `
в•ӯв”Җв”Җв”Җв”Җв”ҖгҖ” GROUP LINK гҖ•в”Җв”Җв”Җв”Җв”Җв”ҲвҠ·
в”Ӯ в—Ұвһӣ Group: ${groupMetadata.subject}
в”Ӯ в—Ұвһӣ Link: ${groupLink}
в•°в”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”ҖвҠ·
`
      await sock.sendMessage(from, {
        text: resultText.trim(),
        contextInfo
      }, { quoted: m })

      logger.success('LINK', `Sent group link for ${groupMetadata.subject}`)

    } catch (e) {
      logger.error('LINK', 'Link command failed', e.message)

      const errorText = `
в•ӯв”Җв”Җв”Җв”Җв”ҖгҖ” ERROR гҖ•в”Җв”Җв”Җв”Җв”Җв”ҲвҠ·
в”Ӯ в—Ұвһӣ Failed to execute
в•°в”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”ҖвҠ·
`
      await sock.sendMessage(from, {
        text: errorText.trim(),
        contextInfo
      }, { quoted: m })
    }
  }
}
/**
 * AstraX - plugins/commands/group management/setgcname.js
 * Set Group Name Command - Change group subject
 * No admin check from router - handles all errors directly
 * Category: group management
 */

export default {
  name: 'setgcname',
  alias: ['setgroupname', 'changename', 'gcname'],
  desc: 'Change group name/subject',
  category: 'group management',
  usage: 'setgcname <new name>',
  permission: 'isOwner',

  async execute(sock, m, args, { db, logger, contextInfo, from, isGroup }) {
    try {
      // в”Җв”Җв”Җ GET PREFIX FROM DB в”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җ
      const prefix = await db.get('prefix') || '.'

      // в”Җв”Җв”Җ CHECK IF GROUP в”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җ
      if (!isGroup) {
        const errorText = `
в•ӯв”Җв”Җв”Җв”Җв”ҖгҖ” ERROR гҖ•в”Җв”Җв”Җв”Җв”Җв”ҲвҠ·
в”Ӯ в—Ұвһӣ Group command only
в•°в”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”ҖвҠ·
`
        return await sock.sendMessage(from, {
          text: errorText.trim(),
          contextInfo
        }, { quoted: m })
      }

      // в”Җв”Җв”Җ VALIDATE NEW NAME в”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җ
      if (!args[0]) {
        const errorText = `
в•ӯв”Җв”Җв”Җв”Җв”ҖгҖ” SETGCNAME гҖ•в”Җв”Җв”Җв”Җв”Җв”ҲвҠ·
в”Ӯ в—Ұвһӣ Usage: ${prefix}setgcname <name>
в”Ӯ в—Ұвһӣ Example: ${prefix}setgcname AstraX Devs
в•°в”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”ҖвҠ·
`
        return await sock.sendMessage(from, {
          text: errorText.trim(),
          contextInfo
        }, { quoted: m })
      }

      const newName = args.join(' ').trim()

      // в”Җв”Җв”Җ VALIDATE NAME LENGTH в”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җ
      if (newName.length > 100) {
        const errorText = `
в•ӯв”Җв”Җв”Җв”Җв”ҖгҖ” ERROR 400 гҖ•в”Җв”Җв”Җв”Җв”Җв”ҲвҠ·
в”Ӯ в—Ұвһӣ Name too long
в”Ӯ в—Ұвһӣ Max: 100 characters
в•°в”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”ҖвҠ·
`
        return await sock.sendMessage(from, {
          text: errorText.trim(),
          contextInfo
        }, { quoted: m })
      }

      if (newName.length < 1) {
        const errorText = `
в•ӯв”Җв”Җв”Җв”Җв”ҖгҖ” ERROR 400 гҖ•в”Җв”Җв”Җв”Җв”Җв”ҲвҠ·
в”Ӯ в—Ұвһӣ Name cannot be empty
в•°в”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”ҖвҠ·
`
        return await sock.sendMessage(from, {
          text: errorText.trim(),
          contextInfo
        }, { quoted: m })
      }

      // в”Җв”Җв”Җ GET GROUP METADATA в”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җ
      let groupMetadata
      try {
        groupMetadata = await sock.groupMetadata(from)
      } catch (e) {
        const errorText = `
в•ӯв”Җв”Җв”Җв”Җв”ҖгҖ” ERROR 403 гҖ•в”Җв”Җв”Җв”Җв”Җв”ҲвҠ·
в”Ӯ в—Ұвһӣ Bot needs to be in group
в•°в”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”ҖвҠ·
`
        return await sock.sendMessage(from, {
          text: errorText.trim(),
          contextInfo
        }, { quoted: m })
      }

      const oldName = groupMetadata.subject

      // в”Җв”Җв”Җ CHECK IF SAME NAME в”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җ
      if (oldName === newName) {
        const errorText = `
в•ӯв”Җв”Җв”Җв”Җв”ҖгҖ” ERROR 409 гҖ•в”Җв”Җв”Җв”Җв”Җв”ҲвҠ·
в”Ӯ в—Ұвһӣ Same as current name
в”Ӯ в—Ұвһӣ No changes made
в•°в”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”ҖвҠ·
`
        return await sock.sendMessage(from, {
          text: errorText.trim(),
          contextInfo
        }, { quoted: m })
      }

      // в”Җв”Җв”Җ EXECUTE NAME CHANGE в”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җ
      try {
        await sock.groupUpdateSubject(from, newName)

        const successText = `
в•ӯв”Җв”Җв”Җв”Җв”ҖгҖ” SUCCESS 200 гҖ•в”Җв”Җв”Җв”Җв”Җв”ҲвҠ·
в”Ӯ в—Ұвһӣ Old Name: ${oldName}
в”Ӯ в—Ұвһӣ New Name: ${newName}
в”Ӯ в—Ұвһӣ Status: Updated вң…
в•°в”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”ҖвҠ·
`
        await sock.sendMessage(from, {
          text: successText.trim(),
          contextInfo
        }, { quoted: m })

        logger.success('SETGCNAME', `Changed group name from "${oldName}" to "${newName}"`)

      } catch (e) {
        const errMsg = e.message || e.toString()
        let errorText = ''

        // 403 - Bot not admin
        if (errMsg.includes('403') || errMsg.includes('forbidden')) {
          errorText = `
в•ӯв”Җв”Җв”Җв”Җв”ҖгҖ” ERROR 403 гҖ•в”Җв”Җв”Җв”Җв”Җв”ҲвҠ·
в”Ӯ в—Ұвһӣ Bot needs admin rights
в•°в”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”ҖвҠ·
`
        }
        // 401 - Not admin / Not authorized
        else if (errMsg.includes('401') || errMsg.includes('not-authorized')) {
          errorText = `
в•ӯв”Җв”Җв”Җв”Җв”ҖгҖ” ERROR 401 гҖ•в”Җв”Җв”Җв”Җв”Җв”ҲвҠ·
в”Ӯ в—Ұвһӣ You need admin rights
в•°в”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”ҖвҠ·
`
        }
        // 400 - Bad request / Invalid name
        else if (errMsg.includes('400') || errMsg.includes('bad-request')) {
          errorText = `
в•ӯв”Җв”Җв”Җв”Җв”ҖгҖ” ERROR 400 гҖ•в”Җв”Җв”Җв”Җв”Җв”ҲвҠ·
в”Ӯ в—Ұвһӣ Invalid group name
в”Ӯ в—Ұвһӣ Contains bad characters
в•°в”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”ҖвҠ·
`
        }
        // 429 - Rate limited
        else if (errMsg.includes('429') || errMsg.includes('rate')) {
          errorText = `
в•ӯв”Җв”Җв”Җв”Җв”ҖгҖ” ERROR 429 гҖ•в”Җв”Җв”Җв”Җв”Җв”ҲвҠ·
в”Ӯ в—Ұвһӣ Too many name changes
в”Ӯ в—Ұвһӣ Wait 24 hours
в•°в”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”ҖвҠ·
`
        }
        // 500 - Server error
        else if (errMsg.includes('500') || errMsg.includes('server')) {
          errorText = `
в•ӯв”Җв”Җв”Җв”Җв”ҖгҖ” ERROR 500 гҖ•в”Җв”Җв”Җв”Җв”Җв”ҲвҠ·
в”Ӯ в—Ұвһӣ WhatsApp server error
в•°в”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”ҖвҠ·
`
        }
        else {
          errorText = `
в•ӯв”Җв”Җв”Җв”Җв”ҖгҖ” ERROR гҖ•в”Җв”Җв”Җв”Җв”Җв”ҲвҠ·
в”Ӯ в—Ұвһӣ Failed to change name
в•°в”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”ҖвҠ·
`
        }

        logger.error('SETGCNAME', 'Failed to update group name', errMsg)
        return await sock.sendMessage(from, {
          text: errorText.trim(),
          contextInfo
        }, { quoted: m })
      }

    } catch (e) {
      logger.error('SETGCNAME', 'Setgcname command failed', e.message)

      const errorText = `
в•ӯв”Җв”Җв”Җв”Җв”ҖгҖ” ERROR гҖ•в”Җв”Җв”Җв”Җв”Җв”ҲвҠ·
в”Ӯ в—Ұвһӣ Failed to execute
в•°в”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”ҖвҠ·
`
      await sock.sendMessage(from, {
        text: errorText.trim(),
        contextInfo
      }, { quoted: m })
    }
  }
}